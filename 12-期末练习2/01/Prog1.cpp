#include <iostream>
#include<cstring>
using namespace std;
int main()
{
        char name[10][21]={"计算机基础","编译原理","算法基础",
                "计算机原理","高等数学","线性代数","离散数学",
                "大学物理","大学英语","大学英语听力"};

        char  searchname[21]; //要查找的书名
        cout<<"请输入要查找的书名:";
        cin.getline(searchname,20);  
        
        /**********Program**********/
        int i, look=0, position;
        for(i=0;i<10;i++)
                if(strcmp(name[i],searchname)==0){
                        look=1;
                        position=i+1;
                }
        if(look==1)
                cout<<searchname<<"存在，其位置是："<<position<<endl;
        else
                cout<<"对不起，"<<searchname<<"不存在！"<<endl;
        /**********  End  **********/
                return 0; 
}
